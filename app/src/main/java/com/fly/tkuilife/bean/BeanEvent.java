package com.fly.tkuilife.bean;

public class BeanEvent {
    private String id, title, kind, class_start, class_end, room, status, sign_start, sign_end;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getKind() {
        return kind;
    }

    public void setKind(String kind) {
        this.kind = kind;
    }

    public String getClass_start() {
        return class_start;
    }

    public void setClass_start(String class_start) {
        this.class_start = class_start;
    }

    public String getClass_end() {
        return class_end;
    }

    public void setClass_end(String class_end) {
        this.class_end = class_end;
    }

    public String getRoom() {
        return room;
    }

    public void setRoom(String room) {
        this.room = room;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public String getSign_start() {
        return sign_start;
    }

    public void setSign_start(String sign_start) {
        this.sign_start = sign_start;
    }

    public String getSign_end() {
        return sign_end;
    }

    public void setSign_end(String sign_end) {
        this.sign_end = sign_end;
    }
}
